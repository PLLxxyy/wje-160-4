/** 每关题目数 */
export const QUESTIONS_PER_LEVEL = 10;

/** 总关卡数 */
export const TOTAL_LEVELS = Math.floor(allQuestions.length / QUESTIONS_PER_LEVEL);

/** 根据关卡获取题目（打乱顺序取10题） */
export function getQuestionsForLevel(level: number): Question[] {
  const start = (level - 1) * QUESTIONS_PER_LEVEL;
  const pool = allQuestions.slice(start, start + QUESTIONS_PER_LEVEL);
  // Fisher-Yates shuffle
  const arr = [...pool];
  for (let i = arr.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [arr[i], arr[j]] = [arr[j], arr[i]];
  }
  return arr;
}
